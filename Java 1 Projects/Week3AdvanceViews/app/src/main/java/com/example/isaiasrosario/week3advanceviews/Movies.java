public class Movies {

   private String movieName;
   private String movieRating;
   private String movieTime;
   private String movieDetail;


   public String getMovieName() {return movieName;}
   public String getMovieRating() {return movieRating;}
   public String getMovieTime() {return movieTime;}
   public String getMovieDetail() {return movieDetail;}

   public void setMovieName(String movieName) {this.movieName = movieName;}
   public void setMovieRating(String movieRating) {this.movieRating = movieRating;}
   public void setMovieTime(String movieTime) {this.movieTime = movieTime;}
   public void setMovieDetail(String movieDetail) {this.movieDetail = movieDetail;}
}
